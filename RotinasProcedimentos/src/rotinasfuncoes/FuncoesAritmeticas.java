package rotinasfuncoes;

/**
 *
 * @author hiw-f
 */
public class FuncoesAritmeticas {
    static int soma ( int a , int b ){
        int s = a + b ;
        return s ;
    }
    
    static int menos ( int a , int b ){
        int s = a - b ;
        return s ;
    }
    
    static int mult ( int a , int b ){
        int s = a * b ;
        return s ;
    }

    static int divide ( int a , int b ){
        int s = a / b ;
        return s;
    }
    
}
